<div class="list-group-item">
  <h3>
     <b> Modelo Estandar de Control Interno (MECI) </b> 
  </h3>
  El propósito esencial del MECI es orientar a las entidades hacia el cumplimiento de sus objetivos y la contribución de éstos a los fines esenciales del Estado, para lo cual se estructura en tres grandes subsistemas, desagregados en sus respectivos componentes y elementos de control.
  En esta sección usted encontrará la estructura básica con la que cuenta la Corporación Autónoma Regional del Atlántico para evaluar el cumplimiento de sus lineamientos estratégicos, las herramientas de gestión y los mecanismos propios para la evaluación de sus procesos.
